		<p style="font-weight:1000; margin-bottom:20px;">We established this website to guide individuals through recent events that took place as a consequence of the Covid-19 global pandemic. The mental health of many individuals have deteriorated following the numerous movement control orders that were placed. Hence, we aim to provide support to those in need during this trying time.</p>
		<p>The National Institute of Mental Health has a mantra, 'No health without mental health." However, due to the stigma that often surrounds mental health millions of people worldwide do not receive the help they need and often overlook this extremely prevalent health issue. According to the National Alliance on Mental Illness, in a given year, one in five, or 18.5% of American adults will experience a mental illness. 
		Some of the most common and frequently reported mental illnesses include depression and bipolar disorders, anxiety, schizophrenia, dementia, and eating disorders. Depression is the number one cause of disability worldwide and is one of the most significant contributors to the global burden of disease, greatly impacting individuals and their families mentally, physically, socially, and financially. Mental illness affects everyone no matter their race, gender, culture, age, ethnicity, or sexual orientation.</p>

		<p>With mental illness being so prevalent both in the United States and worldwide it is important to recognize the early warning signs.</p>

		<ul style="padding-top:20px; margin-left:150px; font-size: 15px;">
		<label>Some activities to look out for include: </label>
			<li>Abnormal eating or sleeping patterns</li>
			<li>Withdrawing from people and typical activities</li>
			<li>A decrease in energy</li>
			<li>Feelings of helplessness or hopelessness</li>
			<li>Feeling confused, forgetful, angry, nervous, or on edge</li>
			<li>An inability to do daily tasks</li>
			<li>Severe mood swings</li>
			<li>Thoughts of self-harm</li>
			<li>Significant increase in drug or alcohol intake.</li>
		</ul>
		
	<p style="margin-top: 30px">Each illness has its own set of signs and symptoms; no two experiences with a mental illness will be the same. Reaching out if think you or someone you know needs help can make all the difference. Your health insurance carrier, a primary care physician or nurse, family, and friends can all be great resources to seek out when taking control of your mental health. The Department of Health and Human Services encourages you to ask questions, stay involved in your health care throughout the process, surround yourself by trusted loved ones, live well with diet and exercise, and maintain activities that you enjoy to better ensure positive mental health. For more information on specific mental illness, warning signs, treatment options, support, and how to help loved ones dealing with mental illness check out these resources; https://www.nami.org/ and https://www.mentalhealth.gov/</p>

	<p style="margin-bottom: 30px">If you, or someone you know is in a crisis, please call the 24/7 suicide prevention hotline; 1-800-273-TALK (8255). They can help those dealing with substance abuse, economic worries, relationship and family problems, sexual orientation, illness, getting over abuse, depression, mental and physical illness, or loneliness.</p>

